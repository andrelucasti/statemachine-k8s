package com.hotel.model;


import org.springframework.data.mongodb.core.mapping.Document;

@Document("hotel_collect")
public class Hotel {

    private Long id;
    private String name;
    private User user;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
